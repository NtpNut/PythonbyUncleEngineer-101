import turtle

turtle.title('Frist Homework')
turtle.bgcolor('black')
t = turtle.Pen()
t.shape('arrow')
t.shapesize(1,1,1)
t.fillcolor('blue')
t.color('green')
t.speed(3)

t.pensize(30)
t.penup()
t.goto(-300, -100)
t.pendown()
t.left(90)
t.forward(350)
t.backward(175)
t.right(90)
t.forward(175)
t.left(90)
t.forward(175)
t.backward(350)

t.pensize(30)
t.penup()
t.goto(0, 250)
t.pendown()
t.left(190)
t.forward(350)
t.left(160)
t.forward(325)
t.left(200)
t.forward(330)
t.left(160)
t.forward(350)

turtle.done()
